class Sum {
    public static void main(String[] args) {
        int i = 1;
        int sum = 0;

        System.out.println("Squares of numbers from 1 to 100:");

        while (i <= 100) {
                       sum =sum+i;
            i++;
        }

        System.out.println("\nSum of number = " + sum);
    }
}
